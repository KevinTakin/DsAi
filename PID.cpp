PID::PID(double 比例系数_, double 积分系数_, double 微分系数_)
    : 比例系数(比例系数_), 积分系数(积分系数_), 微分系数(微分系数_) {
}


double PID::控制循环(double 当前误差, double 时间间隔, double 最近目标宽度, double imgsize) {
    ++加载帧数;
    //把i设置为0，然后根据输出 = Kp×当前误差 + Kd×(当前误差-上一帧误差)/t + 预估偏移
   // std::cout << "当前误差: " << 当前误差 << std::endl;
    // --- 动态阈值计算 ---
    double 宽度占比 = 最近目标宽度 / imgsize;
    double 动态系数 = 最小系数 + (最大系数 - 最小系数) /
        (1.0 + exp(-过渡锐度 * (宽度占比 - 动态过渡中点)));
    double 动态判断阈值 = 动态系数 * 最近目标宽度;


    // === 1. 正常达标入口（误差极小） ===
    if (!已达标 && fabs(当前误差) < 达标误差阈值) {
        已达标 = true;
        积分累计 = 0;
        稳定计数 = 0;
    }

    // === 2. 严重偏离目标：直接退出积分状态 ===

    else if (fabs(当前误差) >= 动态判断阈值) {
        已达标 = false;
        积分累计 = 0;
        稳定计数 = 0;
    }

    // === 3. 模糊区域：可能已经“差不多稳定”但误差还大 ===
    else if (!已达标 && fabs(当前误差) >= 达标误差阈值 && fabs(当前误差) <= 动态判断阈值) {
        // 计算当前误差与上次误差的差值
        double 差值 = fabs(当前误差 - 上一次误差);

        // 如果误差变化小于容限，增加稳定计数
        if (差值 < 误差变化容限) {
            稳定计数++;
        }
        else {
            // 如果误差变化较大，重置稳定计数
            稳定计数 = 0;
        }

        // 如果稳定计数达到要求值，激活积分
        if (稳定计数 >= 最小数据量) {
            已达标 = true;  // 激活积分
            稳定计数 = 0;   // 重置稳定计数
            // 可选：是否需要清零积分取决于你的需求
            积分累计 = 0;    // 重置积分（如果需要）
        }
        else {
        }
    }

    误差变化率 = (当前误差 - 上一次误差) / 时间间隔;

    if (已达标 == true) {
        积分累计 += 当前误差 * 时间间隔;
        比例 = 比例系数 * 当前误差;
        微分 = 微分系数 * 误差变化率;
        微分输出 = atan2(微分, 50) * (200);
    }
    else {
       
        积分累计 += (当前误差*0.1) * 时间间隔;
        比例 = (比例系数 * 0.5) * 当前误差;
        微分 = (微分系数 * 0.5) * 误差变化率;
        微分输出 = atan2(微分, 50) * (200);
    }

    double 积分 = 积分系数 * 积分累计;

    // 原始 PID 输出
    double 原始输出 = 比例 + 积分 + 微分输出;

    // 输出平滑处理
    double 总输出 = 平滑因子 * 原始输出 + (1.0 - 平滑因子) * 上一次平滑输出;
    上一次平滑输出 = 总输出;

    if (已达标 == true) {
        新速度 = ((当前误差 - 上一次误差) * 时间间隔) + (总输出 * 时间间隔) * 速度倍率;
    }
    // 更新状态
    当前速度 = 新速度;
    上一帧速度 = 新速度;
    上一次误差 = 当前误差;

    return 总输出;
}

void PID::更新参数(double 比例系数_, double 积分系数_, double 微分系数_) {
    比例系数 = 比例系数_;
    积分系数 = 积分系数_;
    微分系数 = 微分系数_;

}

double PID::获取速度() const {
    return 当前速度;
}

void PID::重置() {
    总输出 = 0;
    上一次误差 = 0.0;
    上一次平滑输出 = 0;
    积分累计 = 0.0;
    加载帧数 = 0;
    当前速度 = 0.0;
    误差变化率 = 0;
    上一帧速度 = 0.0;
    已达标 = false; // 重置达标状态
    动态判断阈值 = 0;
    稳定计数 = 0;
}

// 修改后的权重值设置函数（新增三个过渡中点参数）

void PID::底层参数(double 达标误差阈值_, double 速度倍率_, double 最小系数_, double 最大系数_, double 过渡锐度_, double 动态过渡中点_, double 最小数据量_, double 误差变化容限_) {
    达标误差阈值 = 达标误差阈值_;
    速度倍率 = 速度倍率_;
    最小系数 = 最小系数_;
    最大系数 = 最大系数_;
    过渡锐度 = 过渡锐度_;
    动态过渡中点 = 动态过渡中点_;
    最小数据量 = 最小数据量_;
    误差变化容限 = 误差变化容限_;
}

void PID::设置平滑因子(double α) {
    // 限制范围在 [0, 1]
    if (α < 0.0) α = 0.0;
    if (α > 1.0) α = 1.0;
    平滑因子 = α;
}
